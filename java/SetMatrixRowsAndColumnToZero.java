    /*WITH ARRAYLIST*/
    private static ArrayList<ArrayList<Integer>> setMatrixZeroesWithArrayList(
            ArrayList<ArrayList<Integer>> list) {
        int columns = list.get(0).size(); // columns
        int rows = list.size(); // rows
        for (int i = 0; i < rows; i++)
        {
            for (int j = 0; j < columns; j++)
            {
                if (0 == list.get(i).get(j))
                {
                    // SCAN ALL ELEMENTS OF ARRAY => MAIN SCAN
                    // WITH A CELL HAVING A ZERO, MARK CORRESPONDING ROW WITH -1,
                    // 		EXCEPT FOR CELLS IN THAT ROW WHICH HAVE A ZERO -
                    // 			IF WE MAKE ALL ZEROES IN THAT ROW ALSO -1, WHEN WE REACH THAT CELL IN OUR MAIN SCAN,
                    //			WE WOULD HAVE ALREADY LOST THE ORIGINAL ZERO ELEMENT CELL AS IT IS NOW -1
                    for (int jj = 0; jj < columns; jj++) // scan for this row
                    {
                        if (list.get(i).get(jj) != 0)
                            list.get(i).set(jj, -1);
                    }

                    System.out.println("\n -----------------------------------\n" + list);
                    System.out.println("\n -----------------------------------\n");
                    // MARK CORRESPONDING COLUMN WITH -1, EXCEPT FOR CELLS WHICH HAVE A ZERO
                    // FOR SAME LOGIC AS ABOVE
                    for (int ii = 0; ii < rows; ii++)	// scan for this column
                    {
                        if (list.get(ii).get(j) != 0)
                            list.get(ii).set(j, -1);
                    }
                }
            }
        }

        // CHANGE ALL -1 CELL ELEMENTS TO 0
        for (int i = 0; i < rows; i++)
        {
            for (int j = 0; j < columns; j++)
            {
                if (-1 == list.get(i).get(j))
                    list.get(i).set(j, 0);
            }
        }
        return list;
    }

